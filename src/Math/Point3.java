/**
 *
 * @author Rafael Gomez
 */
public class Point3 {
    public double x;
    public double y;
    public double z;
    
    public Point3(double x, double y, double z) {
        x = this.x;
        y = this.y;
        z = this.z;
    }
    public double getX() {
        return x;
    }
    public double getY() {
        return y;
    }
    public double getZ() {
        return z;
    }
}
